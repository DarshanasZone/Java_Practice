
/*
HackerRank Java Visitor Pattern

Implement:
1) SumInLeavesVisitor  → sum of leaf values
2) ProductOfRedNodesVisitor → product of RED nodes mod 1e9+7
3) FancyVisitor → |sum(non-leaf even depth) - sum(green leaves)|

Also build the tree from input.
*/


import java.util.ArrayList;
import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

import java.util.ArrayList;
import java.util.Scanner;

enum Color {
    RED, GREEN
}

abstract class Tree {

    private int value;
    private Color color;
    private int depth;

    public Tree(int value, Color color, int depth) {
        this.value = value;
        this.color = color;
        this.depth = depth;
    }

    public int getValue() {
        return value;
    }

    public Color getColor() {
        return color;
    }

    public int getDepth() {
        return depth;
    }

    public abstract void accept(TreeVis visitor);
}

class TreeNode extends Tree {

    private ArrayList<Tree> children = new ArrayList<>();

    public TreeNode(int value, Color color, int depth) {
        super(value, color, depth);
    }

    public void accept(TreeVis visitor) {
        visitor.visitNode(this);

        for (Tree child : children) {
            child.accept(visitor);
        }
    }

    public void addChild(Tree child) {
        children.add(child);
    }
}

class TreeLeaf extends Tree {

    public TreeLeaf(int value, Color color, int depth) {
        super(value, color, depth);
    }

    public void accept(TreeVis visitor) {
        visitor.visitLeaf(this);
    }
}

abstract class TreeVis
{
    public abstract int getResult();
    public abstract void visitNode(TreeNode node);
    public abstract void visitLeaf(TreeLeaf leaf);

}
class SumInLeavesVisitor extends TreeVis {
    private int sum = 0;

    public int getResult() { return sum; }

    public void visitNode(TreeNode node) {}

    public void visitLeaf(TreeLeaf leaf) {
        sum += leaf.getValue();
    }
}

// ---------- VISITOR 2 ----------
class ProductOfRedNodesVisitor extends TreeVis {
    private long product = 1;
    private final int MOD = 1000000007;

    public int getResult() { return (int)product; }

    public void visitNode(TreeNode node) {
        if(node.getColor() == Color.RED)
            product = (product * node.getValue()) % MOD;
    }

    public void visitLeaf(TreeLeaf leaf) {
        if(leaf.getColor() == Color.RED)
            product = (product * leaf.getValue()) % MOD;
    }
}

// ---------- VISITOR 3 ----------
class FancyVisitor extends TreeVis {
    private int evenDepthSum = 0;
    private int greenLeafSum = 0;

    public int getResult() {
        return Math.abs(evenDepthSum - greenLeafSum);
    }

    public void visitNode(TreeNode node) {
        if(node.getDepth() % 2 == 0)
            evenDepthSum += node.getValue();
    }

    public void visitLeaf(TreeLeaf leaf) {
        if(leaf.getColor() == Color.GREEN)
            greenLeafSum += leaf.getValue();
    }
}

// ---------- BUILD TREE ----------
public class JavaVisitorPatternTree {

    static ArrayList<Integer>[] adj;
    static int[] values;
    static Color[] colors;
    static boolean[] visited;

    public static Tree buildTree(int node, int depth) {
        visited[node] = true;
        boolean isLeaf = true;

        TreeNode treeNode = new TreeNode(values[node], colors[node], depth);

        for(int child : adj[node]) {
            if(!visited[child]) {
                isLeaf = false;
                treeNode.addChild(buildTree(child, depth+1));
            }
        }

        if(isLeaf)
            return new TreeLeaf(values[node], colors[node], depth);

        return treeNode;
    }

    public static Tree solve() {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();

        values = new int[n+1];
        for(int i=1;i<=n;i++) values[i]=sc.nextInt();

        colors = new Color[n+1];
        for(int i=1;i<=n;i++)
            colors[i] = sc.nextInt()==0?Color.RED:Color.GREEN;

        adj = new ArrayList[n+1];
        for(int i=1;i<=n;i++) adj[i]=new ArrayList<>();

        for(int i=0;i<n-1;i++){
            int u=sc.nextInt(), v=sc.nextInt();
            adj[u].add(v);
            adj[v].add(u);
        }

        visited = new boolean[n+1];
        return buildTree(1,0);
    }



    public static void main(String[] args) {
      	Tree root = solve();
		SumInLeavesVisitor vis1 = new SumInLeavesVisitor();
      	ProductOfRedNodesVisitor vis2 = new ProductOfRedNodesVisitor();
      	FancyVisitor vis3 = new FancyVisitor();

      	root.accept(vis1);
      	root.accept(vis2);
      	root.accept(vis3);

      	int res1 = vis1.getResult();
      	int res2 = vis2.getResult();
      	int res3 = vis3.getResult();

      	System.out.println(res1);
     	System.out.println(res2);
    	System.out.println(res3);
	}
}